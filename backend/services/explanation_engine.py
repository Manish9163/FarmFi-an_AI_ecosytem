def _confidence_to_certainty(confidence: float) -> str:
    """Convert technical confidence percentage to farmer-friendly certainty."""
    if confidence >= 90:
        return "very_sure"
    elif confidence >= 75:
        return "fairly_sure"
    elif confidence >= 60:
        return "somewhat_sure"
    else:
        return "not_sure"


def _confidence_to_label(confidence: float) -> str:
    """Farmer-friendly label for how sure the system is."""
    if confidence >= 90:
        return "We are very sure about this finding"
    elif confidence >= 75:
        return "We are fairly confident about this detection"
    elif confidence >= 60:
        return "This is our best guess — please verify with an expert"
    else:
        return "We are not very sure — please get this checked by a local expert"


def _severity_to_risk(severity: str) -> dict:
    """Convert severity to farmer-friendly risk info."""
    if severity == "High":
        return {
            "level": "High",
            "emoji": "🚨",
            "color": "#ef4444",
            "label": "Needs Urgent Attention",
            "message": "This needs your attention right away. Act within 1-2 days."
        }
    elif severity == "Medium":
        return {
            "level": "Medium",
            "emoji": "⚠️",
            "color": "#f59e0b",
            "label": "Take Action Soon",
            "message": "Plan to treat this within the next week."
        }
    else:
        return {
            "level": "Low",
            "emoji": "✅",
            "color": "#10b981",
            "label": "Looking Good",
            "message": "No immediate action needed. Keep monitoring."
        }


def explain_prediction(disease_class: str, confidence: float, severity: str) -> dict:
    """
    Main function: Convert raw AI output to farmer-friendly explanation.
    
    Input (from model):
        disease_class: "Tomato___Early_blight"
        confidence: 87.5
        severity: "High"
    
    Output (farmer-friendly):
        Complete explanation dict with no technical terms.
    """
    knowledge = DISEASE_KNOWLEDGE.get(disease_class, _FALLBACK)
    is_healthy = "healthy" in disease_class.lower()
    certainty = _confidence_to_certainty(confidence)
    risk = _severity_to_risk(severity)

    # Build farmer-friendly response
    farmer_response = {
        "disease_name": knowledge["simple_name"],
        "problem_area": knowledge["problem_area"],
        "what_it_means": knowledge["what_it_means"],
        "is_healthy": is_healthy,

        # Risk & severity (simple)
        "risk": risk,
        "urgency": risk["label"],

        # What the farmer should do
        "action_steps": knowledge["action"],
        "organic_option": knowledge["organic_tip"],
        "when_to_worry": knowledge["when_to_worry"],
        "spread_condition": knowledge["spread_condition"],

        # How sure we are (farmer-friendly)
        "certainty": certainty,
        "certainty_label": _confidence_to_label(confidence),

        # Show a "get expert help" banner for low certainty
        "needs_expert_review": confidence < 60,
    }

    # Technical details — ONLY for admin/expert mode
    admin_details = {
        "raw_class": disease_class,
        "confidence_score": round(confidence, 2),
        "severity_level": severity,
        "model_certainty": certainty,
    }

    return {
        "farmer_view": farmer_response,
        "admin_view": admin_details,
    }
